fn main() {
    input!{
        N: usize,
        mut A: [usize; N],
        B: [usize; N],
        mut C: [usize; N],
    }

    A.sort();
    C.sort(); 

    let mut ans = 0;
    for b in &B {
        let a = A.lower_bound(b);
        let c = N - C.upper_bound(b);

        ans += a * c;
    }

    println!("{}", ans);
}


